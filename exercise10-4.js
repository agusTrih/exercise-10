let character = "UP";

switch (character) {
    case "UP":
        console.log("Karakter berjalan keatas");
        break;
    case "RIGHT":
        console.log("Karakter berjalan ke kanan");
        break;
    case "BOTTOM":
        console.log("Karakter berjalan ke bawah");
        break;
    case "LEFT":
        console.log("Karakter berjalan ke kiri");
        break;
}
